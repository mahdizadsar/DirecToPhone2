/******************************************************************************************************/

boolean UdpCommandParser(void){
	uint16 UdpSigPacketCheckSum = 0;
	
	PrintDebug("\n");

	for (i = 0 ; i < 31 ; i++)
		PrintDebug("0x%02X ",((uint8*)&Packet.Command)[i]);
	
	for (i = 0 ; i < 29 ; i++)
		UdpSigPacketCheckSum += ((uint8*)&Packet)[i];
	
	if (UdpSigPacketCheckSum != Packet.CheckSum)   {
		PrintDebug("\nPacket Checksum Error");
		return False;
	}
	
	switch(Packet.Command){
		case REGISTRATION:
			RegistrationFunction();
			break;
		case KEEPALIVE:
			KeepAliveFunction();
			break;
		case CALL:
			CallFunction();
			break;
		case OFFHOOK:
			OffHookFunction();
			break;
		case ONHOOK:
			OnHookFunction();
			break;
		
	}
}

